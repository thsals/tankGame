void CursorView(char show) {
    HANDLE hConsole;
    CONSOLE_CURSOR_INFO ConsoleCursor;

    hConsole = GetStdHandle(STD_OUTPUT_HANDLE);

    ConsoleCursor.bVisible = show;
    ConsoleCursor.dwSize = 1;

    SetConsoleCursorInfo(hConsole, &ConsoleCursor);
}

void goToPoint(int x, int y) {
    COORD Cur;
    Cur.X = x;
    Cur.Y = y;

    SetConsoleCursorPosition(GetStdHandle(STD_OUTPUT_HANDLE), Cur);

}


void drw(int bgColor, int textColor) {

    SetConsoleTextAttribute(GetStdHandle(STD_OUTPUT_HANDLE), bgColor * 16 + textColor);

}

void makeTank(int x,int y) {
    x -= 5;
    y -= 3;
    for (int i = y; i < y+tankSero; i++) {
        for (int j = x; j < x+tankGaro; j++) {
            if (tank[i-y][j-x] == 1) {
                goToPoint(j, i);
                drw(tankColor, tankColor);
                printf(" ");
                drw(CYAN, CYAN);
            }
            else if (tank[i-y][j-x] == 2) {
                goToPoint(j, i);
                drw(BLACK, BLACK);
                printf(" ");
                drw(CYAN, CYAN);
            }
        }
    }
}

